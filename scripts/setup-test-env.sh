#!/bin/bash
# Script to setup test environment for E2E tests
# Registers a test user and updates fixtures with fresh tokens

set -e

API_URL="${VITE_API_URL:-http://localhost:3994}"

echo "🔧 Setting up E2E test environment..."
echo "📡 API URL: $API_URL"

# Register test user (will fail gracefully if already exists)
echo "👤 Registering test user..."
RESPONSE=$(curl -s -X POST "$API_URL/api/auth/register" \
  -H "Content-Type: application/json" \
  -d '{
    "email": "test@example.com",
    "password": "Test12345",
    "name": "Test User"
  }' 2>&1 || true)

# Extract tokens
TOKEN=$(echo "$RESPONSE" | grep -o '"token":"[^"]*' | cut -d'"' -f4)
REFRESH_TOKEN=$(echo "$RESPONSE" | grep -o '"refreshToken":"[^"]*' | cut -d'"' -f4)

if [ -z "$TOKEN" ]; then
  echo "⚠️  Registration failed or user already exists. Trying login..."
  RESPONSE=$(curl -s -X POST "$API_URL/api/auth/login" \
    -H "Content-Type: application/json" \
    -d '{
      "email": "test@example.com",
      "password": "Test12345"
    }')
  
  TOKEN=$(echo "$RESPONSE" | grep -o '"token":"[^"]*' | cut -d'"' -f4)
  REFRESH_TOKEN=$(echo "$RESPONSE" | grep -o '"refreshToken":"[^"]*' | cut -d'"' -f4)
fi

if [ -z "$TOKEN" ]; then
  echo "❌ Failed to get auth tokens!"
  exit 1
fi

echo "✅ Got auth tokens successfully"

# Update fixtures file with fresh tokens
FIXTURES_FILE="e2e/fixtures.ts"

cat > "$FIXTURES_FILE" << EOF
import { test as base, expect } from '@playwright/test';

// Фикстура для E2E тестов - использует реальный бэкенд
// AUTO-GENERATED by scripts/setup-test-env.sh
export const test = base.extend({
  page: async ({ page }, use) => {
    // Set real auth tokens from registered test user
    await page.addInitScript(() => {
      localStorage.setItem('token', '$TOKEN');
      localStorage.setItem('refreshToken', '$REFRESH_TOKEN');
      localStorage.setItem('e2e-test-mode', 'true');
    });

    await use(page);
  },
});

export { expect };
EOF

echo "✅ Updated $FIXTURES_FILE with fresh tokens"
echo ""
echo "🎉 Test environment ready!"
echo "   Run tests with: npx playwright test --project=chromium"
